#include <iostream>
using namespace std;



int main()
{
    cout << "square root of 5.1: ";
    cout << sqrt(900);

    cout << "absolute value of 5.1";
    cout << fabs(5.1);

    cout << "floor: ";
    cout << floor(9.2);

    cout << "Modulous of 2.6, 1.2: ";
    cout << fmod(2.6, 1.2);

    cout << "logarithm of 2.18282";
    cout << log(2.18282);

    cout << "2 to the power of 7: ";
    cout << pow(2, 7);

    cout << "sine of 0";
    cout << sin(0);

    cout << "tangent of 0 in radians";
    cout << tan(0);

}

